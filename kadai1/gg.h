    {
      if (&o != this)
      {
        GgPoints::operator=(o);
        normal = o.normal;
      }
      return *this;
    }

    //! \brief 既存のバッファオブジェクトに頂点の位置データと法線データを転送する.
    //!   \param nv 転送する頂点の位置データの数 (0 ならバッファ全体).
    //!   \param pos 転送元の頂点の位置データが格納されてている領域の先頭のポインタ.
